	ID_ACC VARCHAR(20) NOT NULL,
    ID_ACC_IMAGE INT NOT NULL,
    CONSTRAINT PK_ACCOMODATION_ACC_IMAGE PRIMARY KEY(ID_ACC, ID_ACC_IMAGE),
    CONSTRAINT FK_ACCOMODATION_ACC_IMAGE_ACCOMODATION FOREIGN KEY(ID_ACC) REFERENCES ACCOMODATION(REG_NUM) ON DELETE CASCADE,
    CONSTRAINT FK_ACCOMODATION_ACC_IMAGE_ACCOMODATION_IMAGE FOREIGN KEY(ID_ACC_IMAGE) REFERENCES ACCOMODATION_IMAGE(ID) ON DELETE CASCADE
