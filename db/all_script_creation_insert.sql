    CONSTRAINT PK_ACCOMODATION_PROMO_CODE PRIMARY KEY(ID_ACC, ID_PROMO_CODE),
    CONSTRAINT FK_ACCOMODATION_PROMO_CODE_ACCOMODATION FOREIGN KEY(ID_ACC) REFERENCES ACCOMODATION(REG_NUM) ON DELETE CASCADE,
    CONSTRAINT FK_ACCOMODATION_PROMO_CODE_PROMO_CODE FOREIGN KEY(ID_PROMO_CODE) REFERENCES PROMO_CODE(SERIAL_NUM) ON DELETE CASCADE
