-- IMAGE
CREATE TABLE ACCOMODATION_IMAGE (
    ID INT AUTO_INCREMENT,
    IMG_URL MEDIUMBLOB NOT NULL,
    CONSTRAINT PK_ACCOMODATION_IMAGE PRIMARY KEY (ID)
